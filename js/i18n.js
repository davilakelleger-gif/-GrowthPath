let currentLang = localStorage.getItem('gp-lang') || 'en';

function t(key) {
  return LANG[currentLang]?.[key] ?? LANG['en']?.[key] ?? key;
}

function setLang(lang) {
  if (!LANG[lang]) return;
  currentLang = lang;
  localStorage.setItem('gp-lang', lang);
  document.documentElement.setAttribute('lang', lang);
  translatePage();
}

function translatePage() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.title = t('app_name') + ' — ' + t('tagline');
  const langToggle = document.getElementById('langToggle');
  if (langToggle) langToggle.textContent = currentLang === 'en' ? 'EN' : 'ES';
}

// Auto-translate on load
document.addEventListener('DOMContentLoaded', () => {
  setLang(currentLang);
});
